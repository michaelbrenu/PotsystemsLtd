    <div class="container">
      <span class="tag">Our Process</span>
      <h2 class="section-title">How We <span>Deliver</span></h2>
      <div class="divider"></div>
      <p class="prose" style="max-width:760px;margin-bottom:8px;"><strong>Integrated delivery. Controlled execution. Reliable outcomes.</strong></p>
      <p class="prose" style="max-width:760px;margin-bottom:32px;">Our Seven Steps delivery framework combines architecture, urban planning, engineering, and construction management within a single coordinated approach — from early concept to final handover and aftercare.</p>
      <div style="display:grid;grid-template-columns:1fr;gap:20px;">

        <div class="process-step"><div class="process-num process-num-primary">1</div><div><h4>Project Inception &amp; Strategic Briefing</h4><p class="prose">We begin by defining the project vision, scope, user requirements, site conditions, budget parameters, programme expectations, and success criteria. At this stage, we also establish the governance structure, approvals pathway, key stakeholder interfaces, and overall delivery strategy.</p></div></div>

        <div class="process-step"><div class="process-num process-num-primary">2</div><div><h4>Concept Design &amp; Spatial Planning</h4><p class="prose">Our team develops concept proposals that bring together architecture, urban planning, land use, circulation, infrastructure interface, and functional space planning. This ensures that each project responds not only to client needs, but also to its physical context, regulatory environment, and long-term operational goals.</p></div></div>

        <div class="process-step"><div class="process-num process-num-primary">3</div><div><h4>Technical Design &amp; Cost Planning</h4><p class="prose">Once the concept direction is confirmed, we advance the project into coordinated technical design. This includes architectural detailing, civil and structural systems, MEP coordination, specifications, drawings, Bills of Quantities, and cost plans. Where appropriate, we apply value engineering to improve buildability, performance, and cost efficiency.</p></div></div>

        <div class="process-step"><div class="process-num process-num-primary">4</div><div><h4>Procurement &amp; Mobilisation</h4><p class="prose">Before implementation begins, we prepare the project for controlled delivery through procurement planning, contract packaging, work programming, method statements, HSE planning, logistics coordination, and site readiness reviews. This stage is especially important for projects in occupied, sensitive, or operational environments.</p></div></div>

        <div class="process-step"><div class="process-num process-num-secondary">5</div><div><h4>Construction Management &amp; Site Supervision</h4><p class="prose">During execution, we manage delivery through structured site coordination, contract administration, quality assurance, HSE monitoring, progress tracking, reporting, and change control. Our integrated approach ensures that architectural intent, engineering performance, planning requirements, and project controls remain aligned throughout construction.</p></div></div>

        <div class="process-step"><div class="process-num process-num-secondary">6</div><div><h4>Testing, Commissioning &amp; Handover</h4><p class="prose">As works near completion, we oversee testing, commissioning, snagging, rectification, and final verification of all building and engineering systems. We also provide as-built documentation, operation and maintenance manuals, and user training to support a smooth handover into occupation and use.</p></div></div>

        <div class="process-step"><div class="process-num process-num-secondary">7</div><div><h4>Aftercare &amp; Performance Support</h4><p class="prose">Our involvement continues beyond practical completion through defects liability support, follow-up inspections, technical advisory input, and performance monitoring as required. This helps clients protect asset value, maintain operational performance, and ensure long-term project success.</p></div></div>

      </div>
    </div>
  </section>

